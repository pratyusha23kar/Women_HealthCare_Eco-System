    private void btnRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRequestActionPerformed
        // TODO add your handling code here:
     if(txtFirstName.getText().isEmpty())
            {
                JOptionPane.showMessageDialog(null, "Enter Patient First Name");
                return;
            } 
         if(txtLastName.getText().isEmpty())
            {
                JOptionPane.showMessageDialog(null, "Enter Patient Last Name");
                return;
            } 
         if(txtBlood.getText().isEmpty())
            {
                JOptionPane.showMessageDialog(null, "Enter Patient Blood Group");
                return;
            } 
          if(txtTest.toString().isEmpty())
            {
                JOptionPane.showMessageDialog(null, "Enter Test Required");
                return;
            } 
        
 
        
        String message = txtTest.getText();
        String firstname = txtFirstName.getText();
        String lastname = txtLastName.getText();
        String blood= txtBlood.getText();
        
        LabTestWorkRequest request = new LabTestWorkRequest();
        request.setMessage(message);
        request.setFirstName(firstname);
        request.setLastName(lastname);
        request.setBloodGroup(blood);
        request.setSender(userAccount);
        request.setStatus("Received");
        
        Organization org = null;
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
            if (organization instanceof LabOrganization){
                org = organization;
                break;
            }
        }
        if (org!=null){
            org.getWorkQueue().getWorkRequestList().add(request);
            organization.getWorkQueue().getWorkRequestList().add(request);
        }
        
        JOptionPane.showMessageDialog(this, "Test Requested Successfully!");
    }//GEN-LAST:event_btnRequestActionPerformed

