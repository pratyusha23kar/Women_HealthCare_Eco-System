    private void btnRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRequestActionPerformed
        // TODO add your handling code here:
        if(txtFirstName.getText().isEmpty())
            {
                JOptionPane.showMessageDialog(null, "Enter Patient First Name");
                return;
            } 
         if(txtLastName.getText().isEmpty())
            {
                JOptionPane.showMessageDialog(null, "Enter Patient Last Name");
                return;
            } 
         if(txtMember.getText().isEmpty())
            {
                JOptionPane.showMessageDialog(null, "Enter Patient Member Number");
                return;
            } 
          if(jDateChooser_dob.toString().isEmpty())
            {
                JOptionPane.showMessageDialog(null, "Enter Patient Date Of Birth");
                return;
            } 
           if(txtPhone.getText().isEmpty())
            {
                JOptionPane.showMessageDialog(null, "Enter Patient Phone Number");
                return;
            } 
             if(txtEmail.getText().isEmpty())
            {
                JOptionPane.showMessageDialog(null, "Enter Patient Email Address");
                return;
            } 
         if(txtAmount.getText().isEmpty())
            {
                JOptionPane.showMessageDialog(null, "Enter Claim Amount");
                return;
            } 
         
     
      
        if(Validate()){
            JOptionPane.showMessageDialog(this,"Enter Email Address in Correct Format ");
            return;
        }
         
        
        
        String message = txtMember.getText();
        String firstname = txtFirstName.getText();
        String lastname = txtLastName.getText();
        
        String dob = jDateChooser_dob.getDateFormatString().toString();
        String contactnum = txtPhone.getText();
        String email =txtEmail.getText();
        String amount = txtAmount.getText();
        String serviceType = ComboBox_Service.getSelectedItem().toString();
        
        ReceptionistClaimRequest request = new ReceptionistClaimRequest();
        request.setMessage(message);
        request.setFirstName(firstname);
        request.setLastName(lastname);
        request.setDateOfBirth(dob);
        request.setPhone(contactnum);
        request.setEmail(email);
        request.setAmount(amount);
        request.setServiceType(serviceType);
        request.setSender(userAccount);
        request.setStatus("Received");
        
        Organization org = null;
        
        for(Enterprise enterprise: network.getEnterpriseDirectory().getEnterpriseList())
        {
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList())
        {
            if (organization instanceof ClaimsOrganization)
            {
                org = organization;
                break;
            }
        }
        }
        if (org!=null){
            org.getWorkQueue().getWorkRequestList().add(request);
            organization.getWorkQueue().getWorkRequestList().add(request);
        }
        
        JOptionPane.showMessageDialog(this, "Claim Requested Successfully!");
    }//GEN-LAST:event_btnRequestActionPerformed

