    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
       String userName = txtUserName.getText();
        // Get Password
        char[] passwordCharArray = txtPassword.getPassword();
        String password = String.valueOf(passwordCharArray);

        // Get RoleLookup
        String Rolelookup = DropDown_Role.getSelectedItem().toString();
        UserAccount userAccount = null;
        Network inNetwork = null;
        Enterprise inEnterprise = null;
        Organization inOrganization = null;
        
        if(userName.equals("")){
            JOptionPane.showMessageDialog(null, "Please Enter UserName");
        } else if (password.equals("")){
            JOptionPane.showMessageDialog(null, "Please Enter Password");
        } else {
            if (Rolelookup == "System Admin"){
                userAccount = ecosystem.getUserAccountDirectory().authenticateUser(userName, password);
            } else if (Rolelookup == "Network Admin")
            {
                for (Network network : ecosystem.getNetworkList()) {
                    userAccount = network.getUserAccountDirectory().authenticateUser(userName, password);
                    if (userAccount != null) {
                            inNetwork = network;
                            break;
                        }
                }
            } else if (Rolelookup == "Enterprise Admin")
            {
                boolean Flag= false;
                for (Network network : ecosystem.getNetworkList()) {
                    if(Flag){break;}
                    for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                        userAccount = enterprise.getUserAccountDirectory().authenticateUser(userName, password);
                        if (userAccount != null) {
                            inNetwork = network;
                            inEnterprise = enterprise;
                            Flag = true;
                            break;
                        }
                    }
                }
            } else if (Rolelookup == "Employee Login")
            {
                boolean Flag= false;
                for (Network network : ecosystem.getNetworkList()) {
                    if(Flag){break;}
                    for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                        if(Flag){break;}
                        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                            userAccount = organization.getUserAccountDirectory().authenticateUser(userName, password);
                            if (userAccount != null) {
                                inNetwork = network;
                                inEnterprise = enterprise;
                                inOrganization = organization;
                                Flag = true;
                                break;
                            }

                        } 
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "Please Select the Role!");
            }
 
            if (userAccount == null) {
                JOptionPane.showMessageDialog(null, "Invalid Credentails!");
                return;
            } else {
                //JOptionPane.showMessageDialog(null, "Success !!!");
                CardLayout layout = (CardLayout) container.getLayout();
                //container.add(userAccount.getRole().toString() + "workArea", userAccount.getRole().createWorkArea(container, userAccount, inOrganization, inEnterprise, ecosystem, inNetwork));
                container.add("workArea", userAccount.getRole().createWorkArea(container, userAccount, inOrganization, inEnterprise, ecosystem, inNetwork));
                layout.next(container);
                
                btnLogin.setEnabled(false);
                btnLogout.setEnabled(true);
                txtUserName.setEnabled(false);
                txtPassword.setEnabled(false);
                DropDown_Role.setEnabled(false);
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

