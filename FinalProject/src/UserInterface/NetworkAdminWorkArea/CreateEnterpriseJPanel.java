    public void PopulateEnterpriseType(){
        DropdownEnterpriseType.removeAllItems();
        DropdownEnterpriseType.addItem("--Select--");
        for (Enterprise.EnterpriseType type : Enterprise.EnterpriseType.values()) {
            DropdownEnterpriseType.addItem(type.getValue());
        }
    }
    
    private void populateNetworkTable() {
        DefaultTableModel model = (DefaultTableModel) tbl_Network.getModel();

        model.setRowCount(0);
        Object[] row = new Object[model.getColumnCount()];
        row[0] = network;
        row[1] = network.getLocation();
        model.addRow(row);
        
    }
    
    public void PopulateEnterpriseTable() {
        DefaultTableModel model = (DefaultTableModel) tbl_Enterprise.getModel();

        model.setRowCount(0);
        for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
            for(UserAccount useracount : enterprise.getUserAccountDirectory().getUserAccountList()){
                Object[] row = new Object[model.getColumnCount()];
                row[0] = enterprise;
                row[1] = enterprise.getEnterpriseType().getValue();
                row[2] = useracount.getEmployee().getName();
                row[3] = useracount;
                model.addRow(row);
            }
            if(enterprise.getUserAccountDirectory().getUserAccountList().isEmpty()){
                Object[] row = new Object[model.getColumnCount()];
                row[0] = enterprise;
                row[1] = enterprise.getEnterpriseType().getValue();
                row[2] = "";
                row[3] = "";
                model.addRow(row);
            }
        } 
    }

