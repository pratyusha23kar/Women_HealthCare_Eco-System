    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
       if (txt_EnterpriseName.getText().equals("")) {
            JOptionPane.showMessageDialog(null, Constants.EnterpriseName_valmsg, "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }else if (DropdownEnterpriseType.getSelectedItem().toString().equals("")) {
            JOptionPane.showMessageDialog(null, Constants.Enterprise_type, "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } else {
            if(network.checkIfEnterpriseIsUnique(txt_EnterpriseName.getText(), DropdownEnterpriseType.getSelectedItem().toString()) == false){
                JOptionPane.showMessageDialog(null, "Enterprise information already exists", "Error", JOptionPane.ERROR_MESSAGE);
            }else{
                if(DropdownEnterpriseType.getSelectedItem().toString().equals("Government")){
                    enterprise = network.getEnterpriseDirectory().createAndAddEnterprise(txt_EnterpriseName.getText(), Enterprise.EnterpriseType.Government);
                } else if(DropdownEnterpriseType.getSelectedItem().toString().equals("Hospital")){
                    enterprise = network.getEnterpriseDirectory().createAndAddEnterprise(txt_EnterpriseName.getText(), Enterprise.EnterpriseType.Hospital);
                } else if(DropdownEnterpriseType.getSelectedItem().toString().equals("Woman")){
                    enterprise = network.getEnterpriseDirectory().createAndAddEnterprise(txt_EnterpriseName.getText(), Enterprise.EnterpriseType.Woman);
                } else if(DropdownEnterpriseType.getSelectedItem().toString().equals("Insurance")){
                    enterprise = network.getEnterpriseDirectory().createAndAddEnterprise(txt_EnterpriseName.getText(), Enterprise.EnterpriseType.Insurance);
                } else if(DropdownEnterpriseType.getSelectedItem().toString().equals("Pharmacy")){
                    enterprise = network.getEnterpriseDirectory().createAndAddEnterprise(txt_EnterpriseName.getText(), Enterprise.EnterpriseType.Pharmacy);
                } else {
                    JOptionPane.showMessageDialog(null, "Please select EnterpriseType");
                }
                JOptionPane.showMessageDialog(null, "Enterprise is Created Succesfully");
                PopulateEnterpriseTable();
                txt_EnterpriseName.setText("");
                PopulateEnterpriseType();
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnaddEnterpriseAdminActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddEnterpriseAdminActionPerformed
         int selectedRow = tbl_Enterprise.getSelectedRow();
        
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select any row from Enterprise table", "Error", JOptionPane.ERROR_MESSAGE);
        } else
        {
            Enterprise enterprise = (Enterprise)tbl_Enterprise.getValueAt(selectedRow, 0);
            if(enterprise.getUserAccountDirectory().getUserAccountList().isEmpty()){
                CreateEnterpriseAdminJPanel CreateEnterpriseAdmin = new CreateEnterpriseAdminJPanel(userProcessContainer, enterprise, system);
                userProcessContainer.add("CreateEnterpriseAdminJPanel", CreateEnterpriseAdmin);
                CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                layout.next(userProcessContainer);
            }else{
                JOptionPane.showMessageDialog(null, "Admin Already Present for selected row.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        
        
    }//GEN-LAST:event_btnaddEnterpriseAdminActionPerformed


