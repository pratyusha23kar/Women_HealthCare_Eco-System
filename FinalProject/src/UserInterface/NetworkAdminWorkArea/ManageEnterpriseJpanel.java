    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        int selectedRow = tbl_Enterprise.getSelectedRow();

        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, Constants.Selectrow_tblEnterprise, "Error", JOptionPane.ERROR_MESSAGE);
        } else
        {
            if(tbl_Enterprise.getValueAt(selectedRow, 3).equals("")){
                JOptionPane.showMessageDialog(null, "There is no Admin present for this Enterprise to Update", "Error", JOptionPane.ERROR_MESSAGE);
            }else{
                enterprise = (Enterprise)tbl_Enterprise.getValueAt(selectedRow, 0);
                useraccount = (UserAccount)tbl_Enterprise.getValueAt(selectedRow, 3);
                UpdateEnterpriseJpanel UpdateEnterprise = new UpdateEnterpriseJpanel(userProcessContainer, network, enterprise,useraccount);
                userProcessContainer.add("UpdateEnterpriseJpanel", UpdateEnterprise);
                CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                layout.next(userProcessContainer);
            }
        }
    }//GEN-LAST:event_btn_updateActionPerformed

    private void btn_DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_DeleteActionPerformed
      int selectedRow = tbl_Enterprise.getSelectedRow();
        
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, Constants.Selectrow_tblEnterprise, "Error", JOptionPane.ERROR_MESSAGE);
        } else
        {
            try{
                enterprise = (Enterprise)tbl_Enterprise.getValueAt(selectedRow, 0);
                if(!tbl_Enterprise.getValueAt(selectedRow, 3).equals("")){
                   useraccount = (UserAccount)tbl_Enterprise.getValueAt(selectedRow, 3);
                }
                if(!tbl_Enterprise.getValueAt(selectedRow, 2).equals("")){
                    emp = (Employee)tbl_Enterprise.getValueAt(selectedRow, 2);
                }
                int selectionButton = JOptionPane.YES_NO_OPTION;
                int selectionResult = JOptionPane.showConfirmDialog(null, "Are you sure to delete Network?? It will also delete Network Admin...","Warning",selectionButton);
                if(selectionResult == JOptionPane.YES_OPTION){
                    if(!tbl_Enterprise.getValueAt(selectedRow, 3).equals("")){
                       enterprise.getEmployeeDirectory().deleteEmployee(emp); 
                    }
                    if(!tbl_Enterprise.getValueAt(selectedRow, 2).equals("")){
                        enterprise.getUserAccountDirectory().deleteUserAccount(useraccount);
                    }
                    network.deleteEnterprise(enterprise);
                    PopulateEnterpriseTable();
                    PopulateEnterpriseType();
                }
            }
            catch(IndexOutOfBoundsException IO){
                JOptionPane.showMessageDialog(null, "Cannot Delete the Enterprise", "Error", JOptionPane.ERROR_MESSAGE);
            }
            catch(NullPointerException n){
                JOptionPane.showMessageDialog(null, "Cannot Delete the Enterprise", "Error", JOptionPane.ERROR_MESSAGE);
            }
            catch(RuntimeException re){
                JOptionPane.showMessageDialog(null, "Cannot Delete the Enterprise", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btn_DeleteActionPerformed

