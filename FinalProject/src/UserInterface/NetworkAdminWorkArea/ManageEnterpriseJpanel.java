    private void PopulateEnterpriseTable() {
        DefaultTableModel model = (DefaultTableModel) tbl_Enterprise.getModel();

        model.setRowCount(0);
        for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
            if(enterprise.getEnterpriseType().getValue().equals(DropDown_EnterpriseType.getSelectedItem().toString())){
                for(UserAccount useracount : enterprise.getUserAccountDirectory().getUserAccountList()){
                    Object[] row = new Object[model.getColumnCount()];
                    row[0] = enterprise;
                    row[1] = enterprise.getEnterpriseType().getValue();
                    row[2] = useracount.getEmployee();
                    row[3] = useracount;
                    model.addRow(row);
                }
                if(enterprise.getUserAccountDirectory().getUserAccountList().isEmpty()){
                    Object[] row = new Object[model.getColumnCount()];
                    row[0] = enterprise;
                    row[1] = enterprise.getEnterpriseType().getValue();
                    row[2] = "";
                    row[3] = "";
                    model.addRow(row);
                }
            }   
        }
    }


