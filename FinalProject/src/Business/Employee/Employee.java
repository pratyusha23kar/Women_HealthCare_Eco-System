package Business.Employee;

/**
 *
 * @author praty
 */
public class Employee {
    
    private String name;
    private int id;
    private int age;
    private static int count = 1;

    public Employee() {
        id = count;
        count++;
    }

    public int getId() {
        return id;
    }

    public void setName(String name) {
        this.name = name;
    }

    
    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    
    @Override
    public String toString() {
        return name;
    }
    
    
}
